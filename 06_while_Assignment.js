



//WAP to print numbers from 5 to 15 by incrementing 1
console.log("1. Numbers from 5 to 15 by incrementing 1:");
let i= 5 ;
while (i<=15) {
    console.log(i);
    i++;    
}

//WAP to print numbers from 50 to 40 by decrementing 1
console.log("2. Numbers from 50 to 40 by incrementing 1:");
let j = 50 ;
while (j>=40) {
    console.log(j);
    j--;    
}

